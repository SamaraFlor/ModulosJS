function mostraIdade(nome, idade){
    return 'Á idade de'      + nome +     'é' +   idade;
}

function mostraCidade(nome, cidade){
    return 'a cidade de' +    nome +    'é'       +  cidade;
}


function mostraHobbie(nome, hobbie){
    return 'o hobbie de ', +   nome   +    'é'   +    hobbie;
}

export { mostraIdade,
         mostraCidade,
         mostraHobbie};